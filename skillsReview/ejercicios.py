n1 = 36
n2 = 3

print('A) Division modular = %\nB) Exponenciacion = **\nc) Division que retorne entero = //\n')
print(f'Esto es una Division modular:\n{n1} % {n2} = {n1%35}\n')
print(f'esto es una Exponenciacion:\n{n1} ** {n2} = {n1**36}\n')
print(f'esto es una Divison que retorna entero:\n{n1} // {n2} = {n1//36}\n')